#contagem regressiva para o dias das mães
numero = int(input("Digite o número de dias para o dia das mães: "))
print("Contagem regressiva para o dia das mães:")

#loop para contar regressivamente (usando for e range)
for i in range(numero, 0, -1):
    print(i)

#mensagem final
print("Feliz dia das mães!")  